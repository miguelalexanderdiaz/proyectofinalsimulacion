    private void beginSimulation1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_beginSimulation1MouseClicked
        final SimulacionPuertas p;
        if(cambiarPuertaRButton.isSelected()){
            p = new SimulacionPuertas(this,0);
        }
        else{
            p = new SimulacionPuertas(this,1);
        }
        
        
      


        Thread worker = new Thread() {
            public void run() {
                try {
                    p.run();
                } catch (Exception ex) {
                }
                SwingUtilities.invokeLater(new Runnable() {
                    @Override
                    public void run() {
                        setProgressBar1(0);
                       
                      JOptionPane.showMessageDialog(null, "Simulaci√≥n del problema de las tres puertas ha sido completada", "Info", JOptionPane.INFORMATION_MESSAGE);

                    }
                });
            }
        };
        worker.start();
    }//GEN-LAST:event_beginSimulation1MouseClicked

